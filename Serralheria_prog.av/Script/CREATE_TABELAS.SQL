create database  if not exists serralheria charset utf8mb4;
use serralheria;

CREATE TABLE ENDERECO(
	ID_ENDERECO integer PRIMARY KEY auto_increment not null,
    RUA VARCHAR(50) not null,
    PONTO_REFERENCIA VARCHAR(200) not null,
    ESTADO VARCHAR(50) not null,
    CIDADE VARCHAR(50) not null,
    BAIRRO VARCHAR(50) not null,
    NUMERO VARCHAR(50) not null,
    CEP VARCHAR(50) not null
);
CREATE TABLE PESSOA(
    ID_PESSOA INTEGER auto_increment not null PRIMARY KEY,
	NOME VARCHAR(50) not null,
	TELEFONE VARCHAR(15) not null,
	CPF VARCHAR(15) not null,
	ID_ENDERECO INT(10) not null
    ,FOREIGN KEY (ID_ENDERECO) REFERENCES ENDERECO(ID_ENDERECO)
);
CREATE TABLE CLIENTE(
	ID_CLIENTE INTEGER auto_increment not null PRIMARY KEY,
	ID_PESSOA INT(10) NOT NULL,
    ID_ENDERECO INT(10) not null
    ,FOREIGN KEY (ID_ENDERECO) REFERENCES ENDERECO(ID_ENDERECO)
	,FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA(ID_PESSOA)
);
CREATE TABLE TECNICO(
	ID_TECNICO INTEGER not null auto_increment PRIMARY KEY,
	ID_PESSOA INT(10) NOT NULL,
    CODIGO_ACESSO VARCHAR(10) not null
	,FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA(ID_PESSOA)
);
CREATE TABLE ORCAMENTO(
	ID_ORCAMENTO INTEGER not null auto_increment PRIMARY KEY,
    ALUMINIO VARCHAR(50) not null,
    ALTURA VARCHAR(50) not null,
    LARGURA VARCHAR(50) not null,
    METROS VARCHAR(50) not null,
    TIPO_MATERIAL VARCHAR(50) not null,
    TOTAL VARCHAR(50) not null
);
CREATE TABLE PAGAMENTO(
	ID_PAGAMENTO INTEGER auto_increment not null PRIMARY KEY,
    TIPO_PAGAMENTO VARCHAR(100) not null,
    VALOR_TOTAL VARCHAR(100) not null,
	ID_PESSOA INT(10) NOT NULL
	,FOREIGN KEY (ID_PESSOA) REFERENCES PESSOA(ID_PESSOA)
);



