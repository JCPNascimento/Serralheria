CREATE TABLE ENDERECO(
	ID_ENDERECO INT(10) PRIMARY KEY,
    RUA VARCHAR(500),
    PONTO_REFERENCIA VARCHAR(500),
    ESTADO VARCHAR(500),
    CIDADE VARCHAR(500),
    BAIRRO VARCHAR(500),
    NUMERO VARCHAR(50),
    CEP VARCHAR(50)
);
CREATE TABLE PESSOA(
    ID_PESSOA INT(10) PRIMARY KEY,
	NOME VARCHAR(500),
	TELEFONE VARCHAR(15),
	CPF VARCHAR(15)
);
CREATE TABLE CLIENTE(
	ID_CLIENTE INT(10) PRIMARY KEY,
 	CPF VARCHAR(15),
    TELEFONE VARCHAR(15),
    ID_ENDERECO INT(10)
    ,FOREIGN KEY (ID_ENDERECO) REFERENCES ENDERECO(ID_ENDERECO)
);
CREATE TABLE TECNICO(
	ID_TECNICO INT(10) PRIMARY KEY,
	NOME VARCHAR(500),
    TELEFONE VARCHAR(15),
    CPF VARCHAR(15),
    CODIGO_ACESSO VARCHAR(10)
);
CREATE TABLE ORCAMENTO(
	ID_ORCAMENTO INT(10) PRIMARY KEY,
    ALUMINIO VARCHAR(50),
    ALTURA VARCHAR(50),
    LARGURA VARCHAR(50),
    METROS VARCHAR(50),
    TIPO_MATERIAL VARCHAR(50),
    TOTAL VARCHAR(50)
);
CREATE TABLE PAGAMENTO(
	ID_PAGAMENTO INT(10) PRIMARY KEY,
    TIPO_PAGAMENTO VARCHAR(100),
    VALOR_TOTAL VARCHAR(100),
    VALOR_JUROS VARCHAR(100)
);
