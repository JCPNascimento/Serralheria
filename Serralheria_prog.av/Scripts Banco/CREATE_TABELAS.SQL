CREATE TABLE ENDERECO(
	ID_ENDERECO INT(10) PRIMARY KEY NOT NULL AUTO_INCREMENT,
    RUA VARCHAR(500)  NOT NULL,
    PONTO_REFERENCIA VARCHAR(500),
    ESTADO VARCHAR(500)  NOT NULL,
    CIDADE VARCHAR(500)  NOT NULL,
    BAIRRO VARCHAR(500)  NOT NULL,
    NUMERO VARCHAR(50)  NOT NULL,
    CEP VARCHAR(50)  NOT NULL
);
CREATE TABLE PESSOA(
    ID_PESSOA INT(10)  NOT NULL PRIMARY KEY  AUTO_INCREMENT,
	NOME VARCHAR(500)  NOT NULL,
	TELEFONE VARCHAR(15)  NOT NULL,
	CPF VARCHAR(15)  NOT NULL,
	ID_ENDERECO INT(10)  NOT NULL
	,FOREIGN KEY (ID_ENDERECO) REFERENCES ENDERECO(ID_ENDERECO)
);
CREATE TABLE CLIENTE(
	ID_CLIENTE INT(10)  NOT NULL PRIMARY KEY AUTO_INCREMENT,
 	CPF VARCHAR(15)  NOT NULL,
    TELEFONE VARCHAR(15)  NOT NULL,
    ID_ENDERECO INT(10)  NOT NULL
    ,FOREIGN KEY (ID_ENDERECO) REFERENCES ENDERECO(ID_ENDERECO)
);
CREATE TABLE TECNICO(
	ID_TECNICO INT(10)  NOT NULL PRIMARY KEY AUTO_INCREMENT,
	NOME VARCHAR(500)  NOT NULL,
    TELEFONE VARCHAR(15)  NOT NULL,
    CPF VARCHAR(15)  NOT NULL,
    CODIGO_ACESSO VARCHAR(10)  NOT NULL
);
CREATE TABLE ORCAMENTO(
	ID_ORCAMENTO INT(10)  NOT NULL PRIMARY KEY AUTO_INCREMENT,
    ALUMINIO VARCHAR(50)  NOT NULL,
    ALTURA VARCHAR(50)  NOT NULL,
    LARGURA VARCHAR(50)  NOT NULL,
    METROS VARCHAR(50)  NOT NULL,
    TIPO_MATERIAL VARCHAR(50)  NOT NULL,
    TOTAL VARCHAR(50)  NOT NULL
);
CREATE TABLE PAGAMENTO(
	ID_PAGAMENTO INT(10)  NOT NULL PRIMARY KEY AUTO_INCREMENT,
    TIPO_PAGAMENTO VARCHAR(100)  NOT NULL,
	TOTAL_PARCELAS INT(3) ,
    VALOR_TOTAL VARCHAR(100)  NOT NULL,
    VALOR_JUROS VARCHAR(100)  NOT NULL
);
